package version_7;
 
public class Coord2D {
 
    byte x;
    byte y;
     
    public Coord2D(byte x, byte y) {
        this.x = x;
        this.y = y;
    }
 
    public byte getX() {
        return x;
    }
 
    public void setX(byte x) {
        this.x = x;
    }
 
    public byte getY() {
        return y;
    }
 
    public void setY(byte y) {
        this.y = y;
    }
     
}